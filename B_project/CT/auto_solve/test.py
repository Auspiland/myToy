def solution(n, computers):
    visited = [False] * n
    count = 0

    for start in range(n):
        if visited[start]:
            continue
        # start에서 새로운 컴포넌트 탐색 시작
        count += 1
        stack = [start]
        visited[start] = True

        while stack:
            u = stack.pop()
            row = computers[u]            # 인접 행렬의 u행
            # 인접한 정점 순회
            for v in range(n):
                if row[v] and not visited[v]:
                    visited[v] = True
                    stack.append(v)

    return count



